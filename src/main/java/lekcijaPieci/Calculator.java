package lekcijaPieci;

public class Calculator {

    public int sum (int a, int b) {
        return a+b;
    }

    public double dalisana(double a, double b) {
        return a/b;
    }

    public int reizinasana (int a, int b){
        return a*b;

    }

    public int atnemsana (int a, int b) {
        return a-b;
    }


}
